    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {                                             

        try {
            DiversoController controller = new DiversoController();

            Diverso diverso = new Diverso();

            diverso.setNomeDiverso(txtNomeDiverso.getText());
            diverso.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            diverso.setEstado(comboEstado.getSelectedItem().toString());

            controller.inserir(diverso);
            JOptionPane.showMessageDialog(this, "Item cadastrado com sucesso.");

            txtNomeDiverso.setText("");
            txtQuantidade.setText("");
            comboEstado.setSelectedItem(ABORT);
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar item");
        }

        carregarTabela();
    }                                            

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {                                           

        try {
            DiversoController controller = new DiversoController();

            Diverso diverso = new Diverso();
            diverso.setIdDiverso(Integer.parseInt(labelCodigo.getText()));
            diverso.setNomeDiverso(txtNomeDiverso.getText());
            diverso.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            diverso.setEstado(comboEstado.getSelectedItem().toString());

            controller.alterar(diverso);
            JOptionPane.showMessageDialog(this, "Item alterado com sucesso.");

            labelCodigo.setText("-");
            txtNomeDiverso.setText("");
            txtQuantidade.setText("");
            comboEstado.setSelectedItem(ABORT);
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Erro ao alterar item.");
        }

        carregarTabela();
    }                                          

