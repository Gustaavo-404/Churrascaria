    private void jTableDiversoMouseClicked(java.awt.event.MouseEvent evt) {                                           
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            int linha, coluna = 0;
            linha = jTableDiverso.getSelectedRow();
            String valor = jTableDiverso.getValueAt(linha, coluna).toString();

            try {
                DiversoController controller = new DiversoController();

                Diverso diverso = (Diverso)controller.consultaPorCodigo(Integer.parseInt(valor));

                labelCodigo.setText("" + diverso.getIdDiverso());
                txtNomeDiverso.setText(diverso.getNomeDiverso());
                txtQuantidade.setText(String.valueOf(diverso.getQuantidade()));
                comboEstado.setSelectedItem(diverso.getEstado());

            } catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Erro");
            }
        }
    }                                          

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {                                                   // TODO add your handling code here:
        int linha = jTableDiverso.getSelectedRow();

        if(linha == -1){
            JOptionPane.showMessageDialog(this, "Selecione um item válido para remoção");
            return;
        }

        int codigo = (int) jTableDiverso.getValueAt(linha, 0);

        try {
            Diverso diverso = new Diverso();
            diverso.setIdDiverso(codigo);

            DiversoController controller = new DiversoController();
            controller.excluir(diverso);

            JOptionPane.showMessageDialog(this, "Item excluído com sucesso.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir item: " + ex.getMessage());
        }

        carregarTabela();
    }                                          

