    public void carregarTabela() {
    try {
        jTableDiverso.getTableHeader().setDefaultRenderer(new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(
                    javax.swing.JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                
                java.awt.Component comp = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                comp.setBackground(new java.awt.Color(102, 0, 0));
                comp.setForeground(new java.awt.Color(255, 255, 255)); 
                setFont(new java.awt.Font("Arial Black", Font.BOLD, 14));  
                return comp;
            }
        });

        DiversoController controller = new DiversoController();
        ArrayList<Cadastro> diversos = controller.exibirTodos();

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTableDiverso.getModel();
        model.setRowCount(0);

        for (Cadastro cadastro : diversos) {
            Diverso diverso = (Diverso) cadastro;
            model.addRow(new Object[]{
                diverso.getIdDiverso(),
                diverso.getNomeDiverso(),
                diverso.getQuantidade(),
                diverso.getEstado()
            });
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar os dados: " + ex.getMessage());
    }
}

