    public void carregarTabela() {
    try {
        
        jTableEventos.getTableHeader().setDefaultRenderer(new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(
                    javax.swing.JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                
                java.awt.Component comp = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                comp.setBackground(new java.awt.Color(102, 0, 0));
                comp.setForeground(new java.awt.Color(255, 255, 255)); 
                setFont(new java.awt.Font("Arial Black", Font.BOLD, 14));  
                return comp;
            }
        });

        EventoController controller = new EventoController();
        ArrayList<Cadastro> eventos = controller.exibirTodos();

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTableEventos.getModel();
        model.setRowCount(0);

        for (Cadastro cadastro : eventos) {
            Evento evento = (Evento) cadastro;
            
            String dataEvento = (evento.getDataEvento() != null) ? evento.getDataEvento().toString() : "N/A";
            String horarioInicio = (evento.getHorarioInicio() != null) ? evento.getHorarioInicio().toString() : "Não definido";
            String horarioTermino = (evento.getHorarioTermino() != null) ? evento.getHorarioTermino().toString() : "Não definido";
            
            String horarioInicioFormatado = horarioInicio + "h";
            String horarioTerminoFormatado = horarioTermino + "h";
            
            model.addRow(new Object[] {
                evento.getIdEvento(),
                evento.getNomeEvento(),
                evento.getDescricao(),
                dataEvento,
                horarioInicioFormatado,
                horarioTerminoFormatado
            });
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar os dados: " + ex.getMessage());
    }
}



