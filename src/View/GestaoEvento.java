    private void jTableEventosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEventosMouseClicked

        if(evt.getClickCount() == 2){
            
            int linha = jTableEventos.getSelectedRow();
            String valor = jTableEventos.getValueAt(linha, 0).toString();

            try {
                EventoController controller = new EventoController();

                Evento evento = (Evento)controller.consultaPorCodigo(Integer.parseInt(valor));

                labelCodigo.setText(String.valueOf(evento.getIdEvento()));
                txtNomeEvento.setText(evento.getNomeEvento());
                txtDataEvento.setText(evento.getDataEvento().toString());
                txtDescricao.setText(evento.getDescricao());
                txtHorarioInicio.setText(evento.getHorarioInicio().toString());
                txtHorarioTermino.setText(evento.getHorarioTermino().toString());

            } catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Erro ao carregar o evento: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jTableEventosMouseClicked

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed

        int linha = jTableEventos.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um evento válido para remoção");
            return;
        }

        int codigo = (int) jTableEventos.getValueAt(linha, 0);

        try {
            Evento evento = new Evento();
            evento.setIdEvento(codigo);

            EventoController controller = new EventoController();
            controller.excluir(evento);

            JOptionPane.showMessageDialog(this, "Evento excluído com sucesso.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir evento: " + ex.getMessage());
        }

        carregarTabela();  
    }//GEN-LAST:event_btnRemoverActionPerformed

