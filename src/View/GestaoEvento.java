    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        try {
            EventoController controller = new EventoController();

            Evento evento = new Evento();

            evento.setNomeEvento(txtNomeEvento.getText());
            
            String dataEventoString = txtDataEvento.getText();
            if (dataEventoString != null && !dataEventoString.isEmpty()) {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                java.util.Date utilDate = sdf.parse(dataEventoString);
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                evento.setDataEvento(sqlDate);
            } else {
                throw new Exception("Data do evento não pode ser vazia.");
            }
            
            evento.setDescricao(txtDescricao.getText());
            evento.setHorarioInicio(LocalTime.parse(txtHorarioInicio.getText()));
            evento.setHorarioTermino(LocalTime.parse(txtHorarioTermino.getText()));

            controller.inserir(evento);
            JOptionPane.showMessageDialog(this, "Evento cadastrado com sucesso.");

            txtNomeEvento.setText("");
            txtDataEvento.setText("");
            txtDescricao.setText("");
            txtHorarioInicio.setText("");
            txtHorarioTermino.setText("");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar evento: " + ex.getMessage());
        }

        carregarTabela();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        try {
            EventoController controller = new EventoController();

            Evento evento = new Evento();
            evento.setIdEvento(Integer.parseInt(labelCodigo.getText()));
            
            evento.setNomeEvento(txtNomeEvento.getText());
            
            String dataEventoString = txtDataEvento.getText();
            if (dataEventoString != null && !dataEventoString.isEmpty()) {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                java.util.Date utilDate = sdf.parse(dataEventoString);
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                evento.setDataEvento(sqlDate);
            } else {
                throw new Exception("Data do evento não pode ser vazia.");
            }
            
            evento.setDescricao(txtDescricao.getText());
            evento.setHorarioInicio(LocalTime.parse(txtHorarioInicio.getText()));
            evento.setHorarioTermino(LocalTime.parse(txtHorarioTermino.getText()));

            controller.alterar(evento);
            JOptionPane.showMessageDialog(this, "Evento alterado com sucesso.");

            labelCodigo.setText("-");
            txtNomeEvento.setText("");
            txtDataEvento.setText("");
            txtDescricao.setText("");
            txtHorarioInicio.setText("");
            txtHorarioTermino.setText("");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Erro ao alterar evento: " + ex.getMessage());
        }

        carregarTabela();
    }//GEN-LAST:event_btnAlterarActionPerformed

